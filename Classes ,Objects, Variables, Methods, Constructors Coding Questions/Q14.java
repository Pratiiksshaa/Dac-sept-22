// Java Program to Create a Method with 2 Parameters and without Return Type

class Addition{
	
	static void print(String s1, String s2){
		System.out.println("Names are: "+s1+" and "+s2);
	}

}
class Q14{

	public static void main(String[] args){
		
		Addition.print("Pratiksha", "Sayali");
	}
}

// OUTPUT= Names are: Pratiksha and Sayali